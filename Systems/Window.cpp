Program* Window::program = NULL;

WPARAM Window::Run()
{
	MSG msg = { 0 };

	D3DDesc desc;
	D3D::GetDesc(&desc);


	D3D::Create();
	DirectWrite::Create();
	Keyboard::Create();
	Mouse::Create();

	Time::Create();
	Time::Get()->Start();

	ImGui::Create(desc.Handle, D3D::GetDevice(), D3D::GetDC());
	ImGui::StyleColorsClassic();
	ImGuiNewStyle();

	program = new Program();
	while (true)
	{
		if (PeekMessage(&msg, NULL, 0, 0, PM_REMOVE))
		{
			if (msg.message == WM_QUIT)
				break;

			TranslateMessage(&msg);
			DispatchMessage(&msg);
		}
		else
		{
			Time::Get()->Update();

			//TODO: TPS에서 잠시끄기
		/*	if (ImGui::IsMouseHoveringAnyWindow() == false)
			{*/
				Keyboard::Get()->Update();
				Mouse::Get()->Update();
	/*		}*/

			program->Update();
			ImGui::Update();

			program->PreRender();

			D3D::Get()->SetRenderTarget();
			D3D::Get()->Clear(D3DXCOLOR(0.2,0.2,0.2,1));
			{
				program->Render();
				ImGui::Render();

				//2D영역//
				DirectWrite::GetDC()->BeginDraw();
				{
					program->PostRender();
				}
				DirectWrite::GetDC()->EndDraw();
			}
			D3D::Get()->Present();
		}
	}
	SAFE_DELETE(program);

	ImGui::Delete();
	Time::Delete();
	Mouse::Delete();
	Keyboard::Delete();
	DirectWrite::Delete();
	D3D::Delete();

	return msg.wParam;
}

