#include "Framework.h"
#include "Point.h"

//////////////////////////////////////////////////////////////////////////

const Point Point::Zero = Point(0, 0);

//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief 생성자
//////////////////////////////////////////////////////////////////////////
Point::Point( void )
{
	this->X = 0;
	this->Y = 0;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief 생성자
///@param x : X
///@param y : Y
//////////////////////////////////////////////////////////////////////////
Point::Point( int x, int y )
{
	this->X = x;
	this->Y = y;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief ==
///@param value2 : 포인트
///@return 결과
//////////////////////////////////////////////////////////////////////////
bool Point::operator==( const Point& value2 ) const
{
	if(X == value2.X)
		return Y == value2.Y;
	else
		return false;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief !=
///@param value2 : 포인트
///@return 결과
//////////////////////////////////////////////////////////////////////////
bool Point::operator!=( const Point& value2 ) const
{
	if(X == value2.X)
		return Y != value2.Y;
	else
		return true;
}
//////////////////////////////////////////////////////////////////////////


//////////////////////////////////////////////////////////////////////////
///@brief 문자열로 변환
///@param 문자열
//////////////////////////////////////////////////////////////////////////
std::wstring Point::ToString()
{
	std::string temp;
	char val[255];

	sprintf(val, "{X:%d ", X); temp += val; 
	sprintf(val, "Y:%d}", Y); temp += val;

	return String::ToWString(temp);
}